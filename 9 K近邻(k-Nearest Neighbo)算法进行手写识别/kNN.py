# 图片转向量
def img2vector(filename):
    # 创建一个1024长度的向量
    returnVect = zeros((1, 1024))
    # 打开文件
    fr = open(filename)
    # 循环行列
    for i in range(32):
        lineStr = fr.readline()
        for j in range(32):
            returnVect[0, 32 * i + j] = int(lineStr[j])
    return returnVect

